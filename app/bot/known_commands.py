def normalize_command_token(text: str) -> str | None:
    if not text or not text.startswith("/"):
        return None
    raw = text.split()[0]
    if "@" in raw:
        raw = raw.split("@", 1)[0]
    return raw[1:].lower() if len(raw) > 1 else None
